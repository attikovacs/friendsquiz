    	String wrongAnswer;
    	for (int i = 0; i < ANSWERS - 1; i++) {
    		while (questionId == (wrongAnswerId = ThreadLocalRandom.current().nextInt(0, actors.size())) || answers.contains((wrongAnswer = keys.get(wrongAnswerId)))) {}
    		answers.add(wrongAnswer);
    	}
    	int correctIndex = ThreadLocalRandom.current().nextInt(0, ANSWERS);
        answers.add(correctIndex, questionKey);
        question.setQuestion(actors.get(questionKey));
        question.setAnswers(answers);
        question.setCorrectIndex(correctIndex);

        return question;
